# Tuple
west_africa = ("Ghana","Nigeria","Cote Divoier","Togo","Senegal")
print(west_africa)

# Tuple Length
print(len(west_africa))

# Cheking the Type
print(type(west_africa))

# Creating Tuple form Constructor
east_africa = tuple(("Kenya","Uganda","Ethopia","Tanzania"))
print(east_africa)

# Accesing the Tuple
print(west_africa[0])

# Negative Indexing
print(west_africa[-1])

# Range of Index
print(west_africa[2:4])

# Checking if Item Exist
if "Ghana" in west_africa:
    print("Yes Ghana was there")
else:
    print("Does not exist on map")