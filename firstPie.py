# Set
countries = {"Malta", "Ghana", "Italy", "United Kingdom"}
print(countries)

# True and 1 considered same value
numb = {True, 4, 5, 12, 1, 0, True,}
print(numb)

# Lenght of Set
print(len(countries))

# Type of set
print(type(countries))

#  Creating sets with Constructor
fruits = set({"Apple", "Banana", "Mango"})
print(fruits)